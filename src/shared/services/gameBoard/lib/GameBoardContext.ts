export const generateGameBoardInitialState = ({
    originalBoard,
    sudokuInfo,
}: {
    originalBoard: SudokuBoard;
    sudokuInfo: SudokuModelType;
}): GameBoardStateType => {
    return {
        originalBoard,
        focusedCellPosition: null,
        moves: [originalBoard],
        cellsNotes: new Array(9).fill(new Array(9).fill(new Array(9).fill(0))),
        sudokuInfo,
        duplicates: [],
        isNotesModeActive: false,
    };
};

